package Definicoes;

/**
 *
 * @author Evelyn
 */
public class Cor {

    private int idCor;
    private String Nome;

    public Cor() {
    }

    public Cor(int idCor, String Nome) {
        this.idCor = idCor;
        this.Nome = Nome;
    }

    public int getIdCor() {
        return idCor;
    }

    public void setIdCor(int idCor) {
        this.idCor = idCor;
    }

    public String getNome() {
        return Nome;
    }

    public void setNome(String Nome) {
        this.Nome = Nome;
    }
    
    
    
    
    
}
